export const getMoviesByCategory = (category: string): Movie[] => {
  return moviesData.filter(movie => movie.category === category);
};


export const getMovieById = (id: string): Movie | undefined => {
  return moviesData.find(movie => movie.id === id);
};


export const searchMovies = (searchTerm: string): Movie[] => {
  const term = searchTerm.toLowerCase();
  return moviesData.filter(movie => 
    movie.title.toLowerCase().includes(term) ||
    movie.genre.toLowerCase().includes(term) ||
    movie.synopsis.toLowerCase().includes(term)
  );
};